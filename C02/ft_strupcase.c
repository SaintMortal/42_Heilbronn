#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int		i;	

	i = 0;
	while (str[i])
	{
		if (str[i] >= 97 & str[i] < 123)
		{
			str[i] = str[i] - 32;
		}
		i++;
	}
	return (str);
}
