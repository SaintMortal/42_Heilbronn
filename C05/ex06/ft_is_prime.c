int	ft_is_prime(int nb)
{
	int	flag;
	int	i;

	if (nb < 2)
	{
		return (0);
	}
	i = 1;
	flag = 0;
	while (i <= nb)
	{
		if (nb % i == 0)
		{
			flag++;
		}
		i++;
	}
	if (flag == 2)
		return (1);
	else
		return (0);
}
