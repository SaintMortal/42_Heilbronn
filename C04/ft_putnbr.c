#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;

	c = '0';
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = nb * -1;
	}
	c = (nb % 10) + '0';
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
	}
	write(1, &c, 1);
}

